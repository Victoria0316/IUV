package bluemobi.iuv.eventbus;

/**
 * Created by gaoxy on 2015/9/25.
 * P8_4商场详情页面
 */
public class StoreDetailsEvent extends BaseEvent {

    /**
     * 宝藏的ID
     */
    private String shopID;

    /**
     * 城市
     */
    private String shopDistrictName;

    /**
     * 商场名字
     */
    private String shopName;

    /**
     * 收藏数量
     */
    private String collectionNum;

    /**
     * 点赞数量
     */
    private String praiseNum;

    /**
     * 点赞ID,未点赞为空;
     */
    private String praiseId;



    /**

     * 收藏ID,未收藏为空;
     */
    private String collectionId;

    private String shopLat;
    private String shopLon;

    /**
     *  1 为大型百货 2 为 热门商场
     */
    private int marketType;

    public String getPraiseId() {
        return praiseId;
    }

    public void setPraiseId(String praiseId) {
        this.praiseId = praiseId;
    }

    public String getCollectionId() {
        return collectionId;
    }

    public void setCollectionId(String collectionId) {
        this.collectionId = collectionId;
    }

    public String getPraiseNum() {
        return praiseNum;
    }

    public void setPraiseNum(String praiseNum) {
        this.praiseNum = praiseNum;
    }

    public String getCollectionNum() {
        return collectionNum;
    }

    public void setCollectionNum(String collectionNum) {
        this.collectionNum = collectionNum;
    }

    public String getShopName() {
        return shopName;
    }

    public void setShopName(String shopName) {
        this.shopName = shopName;
    }

    public String getShopDistrictName() {
        return shopDistrictName;
    }

    public void setShopDistrictName(String shopDistrictName) {
        this.shopDistrictName = shopDistrictName;
    }

    public String getShopID() {
        return shopID;
    }

    public void setShopID(String shopID) {
        this.shopID = shopID;
    }

    public String getShopLat() {
        return shopLat;
    }

    public void setShopLat(String shopLat) {
        this.shopLat = shopLat;
    }

    public String getShopLon() {
        return shopLon;
    }

    public void setShopLon(String shopLon) {
        this.shopLon = shopLon;
    }


    public int getMarketType() {
        return marketType;
    }

    public void setMarketType(int marketType) {
        this.marketType = marketType;
    }
}
