package bluemobi.iuv.network.model;

import java.util.List;

/**
 * Created by gaoxy on 2015/10/8.
 */
public class HotShopListMoedl {

    String currentpage;
    String pageTime;
    String totalnum;
    String totalpage;
    private List<HotShopInfo> info;

    public String getCurrentpage() {
        int i = Integer.parseInt(currentpage)-1;
        return String.valueOf(i);
    }

    public void setCurrentpage(String currentpage) {
        this.currentpage = currentpage;
    }

    public String getPageTime() {
        return pageTime;
    }

    public void setPageTime(String pageTime) {
        this.pageTime = pageTime;
    }

    public String getTotalnum() {
        return totalnum;
    }

    public void setTotalnum(String totalnum) {
        this.totalnum = totalnum;
    }

    public String getTotalpage() {
        return totalpage;
    }

    public void setTotalpage(String totalpage) {
        this.totalpage = totalpage;
    }

    public List<HotShopInfo> getInfo() {
        return info;
    }

    public void setInfo(List<HotShopInfo> info) {
        this.info = info;
    }
}
