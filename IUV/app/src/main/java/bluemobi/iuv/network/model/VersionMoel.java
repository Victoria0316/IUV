public class VersionMoel {
    private String platform;//平台类型 ('WEB','IPHONE','APHONE','IPAD','IOS','ANDROID','APAD')
    private String productName ;//产品名称

    private float V_OLD_CODE ;//原版本号
    private float vcode ;//版本号
    private String vname ;//版本名称
    private String content ;// 版本详情
    private String vtype ;//版本类型（1，测试版，2正式版
    private String updateState ;//更新状态（1，无更新，2普通更新，3强制更新）
    private String MD5_CODE ;//MD5码

    private String filepath  ;//文件路径
    private String FILE_SIZE  ;//安装包大小
    private String FILEPATH_TDC  ;//二维码图片路径
    private String ctime  ;// 创建时间
    private String MTIME  ;//最后一次更新时间
    private String releaseStatus  ;//发布状态。1：已发布；0：未发布
    private String status  ;//逻辑删除字段(0:正常数据，1:已删除数据)
    private String OPT_TIME ;//操作时间
    private String optTime ;// 备注

    public String getPlatform() {
        return platform;
    }

    public void setPlatform(String platform) {
        this.platform = platform;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public float getV_OLD_CODE() {
        return V_OLD_CODE;
    }

    public void setV_OLD_CODE(float v_OLD_CODE) {
        V_OLD_CODE = v_OLD_CODE;
    }

    public float getVcode() {
        return vcode;
    }

    public void setVcode(float vcode) {
        this.vcode = vcode;
    }

    public String getVname() {
        return vname;
    }

    public void setVname(String vname) {
        this.vname = vname;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getVtype() {
        return vtype;
    }

    public void setVtype(String vtype) {
        this.vtype = vtype;
    }

    public String getUpdateState() {
        return updateState;
    }

    public void setUpdateState(String updateState) {
        this.updateState = updateState;
    }

    public String getMD5_CODE() {
        return MD5_CODE;
    }

    public void setMD5_CODE(String MD5_CODE) {
        this.MD5_CODE = MD5_CODE;
    }

    public String getFilepath() {
        return filepath;
    }

    public void setFilepath(String filepath) {
        this.filepath = filepath;
    }

    public String getFILE_SIZE() {
        return FILE_SIZE;
    }

    public void setFILE_SIZE(String FILE_SIZE) {
        this.FILE_SIZE = FILE_SIZE;
    }

    public String getFILEPATH_TDC() {
        return FILEPATH_TDC;
    }

    public void setFILEPATH_TDC(String FILEPATH_TDC) {
        this.FILEPATH_TDC = FILEPATH_TDC;
    }

    public String getCtime() {
        return ctime;
    }

    public void setCtime(String ctime) {
        this.ctime = ctime;
    }

    public String getMTIME() {
        return MTIME;
    }

    public void setMTIME(String MTIME) {
        this.MTIME = MTIME;
    }

    public String getReleaseStatus() {
        return releaseStatus;
    }

    public void setReleaseStatus(String releaseStatus) {
        this.releaseStatus = releaseStatus;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getOPT_TIME() {
        return OPT_TIME;
    }

    public void setOPT_TIME(String OPT_TIME) {
        this.OPT_TIME = OPT_TIME;
    }

    public String getOptTime() {
        return optTime;
    }

    public void setOptTime(String optTime) {
        this.optTime = optTime;
    }
}
